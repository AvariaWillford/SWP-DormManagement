    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
          String type = request.getParameter("type");
        String name = request.getParameter("name");

        DAObed dao = new DAObed();
        if (type != null && name != null) {
            if (type.equals("bed")) {
                model.bed bedResult = dao.searchBedsByName(name);
                request.setAttribute("bedResult", bedResult);
            } else if (type.equals("room")) {
                room roomResult = dao.searchBedsByRoom(name);
                request.setAttribute("roomResult", roomResult);
            }
        }

        request.getRequestDispatcher("/bed.jsp").forward(request, response);
    }

